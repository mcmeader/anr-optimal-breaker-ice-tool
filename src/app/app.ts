  private getCreditInterfaceValues(text: string) {
    const costMatch =
      text.match(/Interface\s*→\s*(?:<strong>)?(\d+|X)\[credit\]:(?:<\/strong>)?/i) ||
      text.match(/(?:<strong>)?(\d+|X)\[credit\]:(?:<\/strong>)?/i);

    if (!costMatch) return null;

    const cost = costMatch[1] === 'X' ? -2 : Number(costMatch[1]);
    const afterCost = text.slice(costMatch.index);

    const breakPatterns = [
      // Break any number of <strong>barrier</strong> subroutines
      /Break(?:\s+up\s+to)?\s*(any number|\d+|X)\s*(?:of\s+)?(?:<strong>[^<]+<\/strong>\s*)+subroutines?/i,

      // Break 1 <strong>sentry</strong> or <strong>barrier</strong> subroutine
      /Break(?:\s+up\s+to)?\s*(any number|\d+|X)\s*(?:<strong>[^<]+<\/strong>\s*(?:or|and)\s*)+<strong>[^<]+<\/strong>\s*subroutines?/i,

      // Break 1 <strong>code gate</strong> or 1 <strong>barrier</strong> subroutine
      /Break(?:\s+up\s+to)?\s*(any number|\d+|X)\s*<strong>[^<]+<\/strong>\s*(?:or|and)\s*(?:any number|\d+|X)\s*<strong>[^<]+<\/strong>\s*subroutines?/i,

      // Break 1 subroutine
      /Break(?:\s+up\s+to)?\s*(any number|\d+|X)\s*subroutines?/i,

      // +2 strength. Then ... break up to 2 subroutines
      /\+[X\d]+\s*strength\.[\s\S]*?break(?:\s+up\s+to)?\s*(any number|\d+|X)\s*subroutines?/i,
    ];

    let rawBreak = null;

    for (const pattern of breakPatterns) {
      const match = afterCost.match(pattern);
      if (match) {
        rawBreak = match[1];
        break;
      }
    }

    if (!rawBreak) return null;

    const breakCount =
      rawBreak.toLowerCase() === 'any number' ? 100 : rawBreak === 'X' ? -2 : Number(rawBreak);

    return [cost, breakCount];
  }
