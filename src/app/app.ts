  getUniqueValues(vals: ApiCard[]) {
    return vals.filter(
      (value, index, self) =>
        self.findIndex(
          (item) => item.stripped_title.toLowerCase() === value.stripped_title.toLowerCase(),
        ) === index,
    );
  }

